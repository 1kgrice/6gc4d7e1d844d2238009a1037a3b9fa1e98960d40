    def self.path_list(**args)
      skip_root = args.fetch :skip_root, true
      category = args.fetch :category, ""
      include_names = args.fetch :include_names, false

      if category.empty?
        dig tree, skip_root:, include_names:
      else
        dig tree[category.to_sym], skip_root:, include_names:
      end
    end

    def self.submenu_path_list(**args)
      category = args.fetch :category, ""
      path_list(skip_root: false, category:) - path_list(skip_root: true, category:)
    end

    def self.dig(hsh, **args)
      skip_root = args.fetch :skip_root, true
      parent = args.fetch :parent, ""
      include_names = args.fetch :include_names, false

      format_value = ->(kk, pp, cc, par) do
        return pp unless include_names
        is_root = (kk == :_root)
        { name: (is_root ? par : kk), path: pp, count: cc, is_root: }
      end

      items = []
      if hsh.is_a?(Hash)
        hsh.each do |key, value|
          next if key == :_root && skip_root
          if value.is_a?(Hash)
            items << format_value.call(key, value[:path], value[:count], parent)
          elsif value.is_a?(Array)
            args[:parent] = parent
            value.each { |item| items << dig(item, skip_root:, parent: key, include_names:) }
          end
        end
      elsif hsh.is_a?(Array)
        hsh.each { |item| items << dig(item, skip_root:, include_names:) }
      end
      items.flatten
    end

